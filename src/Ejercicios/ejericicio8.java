package Ejercicios;

public class ejericicio8 {
    /*
    Dado que la temperatura es c = 25 grados Celsius,
    conviértela a Fahrenheit usando la fórmula: f=(9/5*C)+32
     */
    public static void main(String[] args) {
        int c = 25, a = 0;
        a = ((9*c)/5)+32;
        System.out.println("En Fahrenheit es: "+a);
    }
}
